    private void updateTotal() {
    DefaultTableModel model = (DefaultTableModel) jTable_1.getModel();
    double sum = 0;
    for (int i = 0; i < model.getRowCount(); i++) {
        double price = (Double) model.getValueAt(i, 2);
        sum += price;
    }
    jtxtTotal.setText("$" + String.format("%.2f", sum));
}

